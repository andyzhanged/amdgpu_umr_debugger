#include "umrapp.h"
#include <inttypes.h>

int umr_read_vram(struct umr_asic *asic, uint32_t vmid, uint64_t address, uint32_t size, void *dst)
{
	if (vmid == 0xFFFF) {
		// addressing is physical
		lseek(asic->fd.vram, address, SEEK_SET);
		read(asic->fd.vram, dst, size);
		return 0;
	}
	return 0;
}
